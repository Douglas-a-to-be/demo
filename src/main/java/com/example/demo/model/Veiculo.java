package com.example.demo.model;

public class Veiculo {

    private int rodas;
    private double peso;
    private int eixos;

    /**
     * @return the rodas
     */
    public int getRodas() {

        return rodas;
    }

    /**
     * @param rodas the rodas to set
     */
    public void setRodas(int rodas) {

        this.rodas = rodas;
    }

    /**
     * @return the peso
     */
    public double getPeso() {

        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(double peso) {

        this.peso = peso;
    }

    /**
     * @return the eixos
     */
    public int getEixos() {

        return eixos;
    }

    /**
     * @param eixos the eixos to set
     */
    public void setEixos(int eixos) {

        this.eixos = eixos;
    }

}
